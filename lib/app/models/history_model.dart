class HistoryModel {
  final String id;
  final String title;
  final String subtitle;
  final String source;
  final String timeAgo;
  final String videoUrl;
  final String thumbnailUrl;

  HistoryModel({
    required this.id,
    required this.title,
    required this.subtitle,
    required this.source,
    required this.timeAgo,
    required this.videoUrl,
    required this.thumbnailUrl,
  });

}